package Expressions;

public class Main {

	public static void main(String[] args) {
		
		int friends = 10;
		
		++friends;
		System.out.println(friends);
		
		friends += 3;
		System.out.println(friends);
		
		System.out.println(friends % 3);
		
		friends /= 3;
		System.out.println(friends);
		
		
		double x = 15;
		x /= 2;
		System.out.println(x);
		
	}

}
